
// GRAPHIC DESIGN MODALS

function hideshowSocialIssueModal(){
	$('.social-issue-modal-btn').click(function(){
		$('.social-issue-modal').addClass('modal-open');
	});
	$('.modal-close-icon').click(function(){
		$('.social-issue-modal').removeClass('modal-open');
	});
}
$(document).ready(function() {
	hideshowSocialIssueModal();
});

